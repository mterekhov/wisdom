import UIKit

extension UIColor {
    
    static func wisdom_white() -> UIColor {
        return .white
    }

    static func wisdom_red() -> UIColor {
        return UIColor(red: 0.96, green: 0.63, blue: 0.51, alpha: 1.0)
    }

    static func wisdom_gray() -> UIColor {
        return UIColor(red: 0.97, green: 0.97, blue: 0.97, alpha: 1.0)
    }
    
    static func wisdom_green() -> UIColor {
        return UIColor(red: 0.82, green: 1.0, blue: 0.82, alpha: 1.0)
    }
    
    static func wisdom_yellow() -> UIColor {
        return UIColor(red: 0.98, green: 0.94, blue: 0.74, alpha: 1.0)
    }
    
}
